    private void toggleSearchFields() {
    if (jRadioButton1.isSelected()) { // Tìm theo tên
        jTextField1.setEnabled(true);
        jTextField2.setEnabled(false);
        jTextField3.setEnabled(false);
        jTextField2.setText(""); // Xóa nội dung các trường không dùng
        jTextField3.setText("");
    } else if (jRadioButton2.isSelected()) { // Tìm theo giá
        jTextField1.setEnabled(false);
        jTextField2.setEnabled(true);
        jTextField3.setEnabled(true);
        jTextField1.setText(""); // Xóa nội dung các trường không dùng
    }
}
    private void loadAllProducts() {
        try {
            ResultSet rs = sp.ShowSanpham();
            fillTable(rs);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu sản phẩm: " + e.getMessage());
        }
    }
    private void fillTable(ResultSet rs) throws SQLException {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("MaSP"),
                rs.getString("TenSP"),
                rs.getInt("Dongia"),
                rs.getString("Tenloai")
            });
        }
    }

