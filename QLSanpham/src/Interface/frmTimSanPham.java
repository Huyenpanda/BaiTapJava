    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
        ResultSet rs = null;
        if (jRadioButton1.isSelected()) { // Tìm theo tên sản phẩm
            String tenSP = jTextField1.getText();
            rs = sp.ShowSPTheoten(tenSP);
            if (!rs.isBeforeFirst()) { // Kiểm tra nếu không có dòng nào trong ResultSet
                JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm tên " + tenSP, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        } else if (jRadioButton2.isSelected()) { // Tìm theo khoảng giá
            int giaTu = Integer.parseInt(jTextField2.getText());
            int giaDen = Integer.parseInt(jTextField3.getText());
            rs = sp.ShowSPTheogia(giaTu, giaDen);
            if (!rs.isBeforeFirst()) { // Kiểm tra nếu không có dòng nào trong ResultSet
                JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm trong khoảng giá từ " + giaTu + " đến " + giaDen, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn phương thức tìm kiếm.");
            return;
        }
        fillTable(rs);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Giá phải là số !");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Lỗi khi tìm kiếm sản phẩm: " + e.getMessage());
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

